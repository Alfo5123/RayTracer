
public class Point3D
{
	private float x ; 
	private float y ; 
	private float z ; 
	
	public Point3D (){}; 
	
	public Point3D ( float x , float y , float z )
	{
		this.x = x ; 
		this.y = y ; 
		this.z = z ; 
	};
	
	public float getX ( )
	{
		return this.x ; 
	};
	
	public float getY ( )
	{
		return this.y ;
	};
	
	public float getZ ( )
	{
		return this.z ;
	};
}
